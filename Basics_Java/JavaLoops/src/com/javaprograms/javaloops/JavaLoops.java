package com.javaprograms.javaloops;

public class JavaLoops {
    /**
     *
     * @ for loop
     */
    public static void main(String[] args) {
        for (int i=0;i<=10;i++) {
            System.out.println("Hello World!"+i);
        }
    /**
     * while loop
     */
        int value=10;
        while (value<20){
            System.out.println(value+" smaller than 20");
            value+=1;

        }
    }
}
